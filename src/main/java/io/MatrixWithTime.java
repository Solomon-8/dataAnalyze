public class MatrixWithTime<T> {

    private Date time;

    private T[][] data;

    public MatrixWithTime() {
    }

    public MatrixWithTime(Date time, T[][] data) {
        this.time = time;
        this.data = data;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }

    public T[][] getData() {
        return data;
    }

    public void setData(T[][] data) {
        this.data = data;
    }
}
