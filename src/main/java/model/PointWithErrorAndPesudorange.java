public class PointWithErrorAndPesudorange {

    private double x;
    private double y;
    private double z;
    private double error;
    private double pesudorange;

    public PointWithErrorAndPesudorange() {
    }

    public PointWithErrorAndPesudorange(double x, double y, double z, double error, double pesudorange) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.error = error;
        this.pesudorange = pesudorange;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getZ() {
        return z;
    }

    public void setZ(double z) {
        this.z = z;
    }

    public double getError() {
        return error;
    }

    public void setError(double error) {
        this.error = error;
    }

    public double getPesudorange() {
        return pesudorange;
    }

    public void setPesudorange(double pesudorange) {
        this.pesudorange = pesudorange;
    }

    @Override
    public String toString() {
        return x + " " + y + " " + z + " " + error + " " + pesudorange;
    }
}
