public class PointE {
    private double ex;
    private double ey;
    private double ez;

    public PointE() {
    }

    public PointE(double ex, double ey, double ez) {
        this.ex = ex;
        this.ey = ey;
        this.ez = ez;
    }

    public double getEx() {
        return ex;
    }

    public void setEx(double ex) {
        this.ex = ex;
    }

    public double getEy() {
        return ey;
    }

    public void setEy(double ey) {
        this.ey = ey;
    }

    public double getEz() {
        return ez;
    }

    public void setEz(double ez) {
        this.ez = ez;
    }
}
